/** Format a number as Canadian dollar currency */
export function formatPrice(value: number): string {
  if (value >= 1_000_000) {
    const m = value / 1_000_000;
    // show one decimal only if not a whole number
    return `$${m % 1 === 0 ? m.toFixed(0) : m.toFixed(1)}M`;
  }
  if (value >= 1_000) {
    return `$${Math.round(value / 1_000)}K`;
  }
  return `$${value.toLocaleString("en-CA")}`;
}

/** Full dollar format with commas — for property detail views */
export function formatPriceFull(value: number): string {
  return value.toLocaleString("en-CA", { style: "currency", currency: "CAD", maximumFractionDigits: 0 });
}

/** Convert ISO date string to relative time label */
export function relativeDays(isoDate: string): string {
  const days = Math.floor((Date.now() - new Date(isoDate).getTime()) / 86_400_000);
  if (days === 0) return "Listed today";
  if (days === 1) return "Listed yesterday";
  return `Listed ${days} days ago`;
}
